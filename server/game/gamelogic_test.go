func TestCheckwin(t *testing.T) {
	//if gamelogic is correct Checkwin returns true
	//horizontal tests
	if Checkwin(test1()) {
		t.Error("error in test1")
	}
	if Checkwin(test2()) == false {
		t.Error("error in test2")
	}
	if Checkwin(test3()) != false {
		t.Error("error in test3")
	}
	if Checkwin(test4()) == false {
		t.Error("error in test3")
	}
	if Checkwin(test5()) == false {
		t.Error("error in test5")
	}
	//vertical tests
	if Checkwin(test1()) {
		t.Error("error in vertical test")
	}
	if Checkwin(test6()) == false {
		t.Error("error in test6")
	}
	if Checkwin(test7()) == false {
		t.Error("error in test7")
	}
	//diagonal test
	if Checkwin(test8()) == false {
		t.Error("error in test7")
	}
}
